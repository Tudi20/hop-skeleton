
public class Jatekos {
	private int eletero;
	private boolean specialisTamadas;
	private boolean iszomEgyet;
	private int jatekosAzonosito;
	
	public int getEletero() {
		return eletero;
	}
	
	public void setEletero(int eletero) {
		this.eletero = eletero;
	}
	
	public boolean getSpecialisTamadas() {
		return specialisTamadas;
	}
	
	public void setSpecialisTamadas(boolean specialisTamadas) {
		this.specialisTamadas = specialisTamadas;
	}
	
	public boolean getIszomEgyet() {
		return iszomEgyet;
	}
	
	public void setIszomEgyet(boolean iszomEgyet) {
		this.iszomEgyet = iszomEgyet;
	}
	
	public int getJatekosAzonosito() {
		return jatekosAzonosito;
	}
}
